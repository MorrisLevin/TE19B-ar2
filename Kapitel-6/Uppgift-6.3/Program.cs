using System;

namespace Uppgift_6._3
{
    class Program
    {
        static void Main(string[] args)
        {
            SkrivTalKvadratMellan(5, 10);

        }
        static void SkrivTalKvadratMellan(int nedreGräns, int övreGräns)
        {
            for (int i = nedreGräns; i <= övreGräns; i++)
            {
                SkrivTalKvadratMellan[i]
            }
        }
        static void SkrivTalIKvadratMellan(int tal)
        {
            Console.WriteLine($"Talet {tal} upphöjt till två blir {tal * tal}");
        }
    }
}
